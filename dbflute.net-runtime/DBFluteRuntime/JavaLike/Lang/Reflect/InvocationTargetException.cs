
namespace DBFluteRuntime.JavaLike.Lang.Reflect
{
    /// <summary>
    /// [Java]InvocationTargetException
    /// </summary>
    public class InvocationTargetException : System.Exception
    {
        public InvocationTargetException() : base() { }
        public InvocationTargetException(string message) : base(message) { }
        public InvocationTargetException(string message, System.Exception innerException) : base(message, innerException) { }
    }
}
